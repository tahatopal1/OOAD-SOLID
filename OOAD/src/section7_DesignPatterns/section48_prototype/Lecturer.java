package section7_DesignPatterns.section48_prototype;

public class Lecturer {
	
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Lecturer [name=" + name + "]";
	}
	
	
	
}
